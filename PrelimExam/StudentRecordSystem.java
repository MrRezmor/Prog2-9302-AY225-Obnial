    private double computePrelimGrade(double lab1, double lab2, double lab3, double prelim, double attendanceGrade) {
        double labAverage = (lab1 + lab2 + lab3) / 3.0;
        double classStanding = (attendanceGrade * 0.40) + (labAverage * 0.60);
        double prelimGrade = (prelim * 0.30) + (classStanding * 0.70);
        return prelimGrade;
    }
    

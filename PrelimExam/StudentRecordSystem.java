    private void addStudent() {
        String studentID = txtStudentID.getText().trim();
        String firstName = txtFirstName.getText().trim();
        String lastName = txtLastName.getText().trim();
        String lab1Str = txtLab1.getText().trim();
        String lab2Str = txtLab2.getText().trim();
        String lab3Str = txtLab3.getText().trim();
        String prelimStr = txtPrelim.getText().trim();
        String attendanceGradeStr = txtAttendanceGrade.getText().trim();
        
        if (studentID.isEmpty() || firstName.isEmpty() || lastName.isEmpty()) {
            JOptionPane.showMessageDialog(this, 
                "Student ID, First Name, and Last Name are required!",
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        double lab1 = lab1Str.isEmpty() ? 0 : Double.parseDouble(lab1Str);
        double lab2 = lab2Str.isEmpty() ? 0 : Double.parseDouble(lab2Str);
        double lab3 = lab3Str.isEmpty() ? 0 : Double.parseDouble(lab3Str);
        double prelim = prelimStr.isEmpty() ? 0 : Double.parseDouble(prelimStr);
        double attendanceGrade = attendanceGradeStr.isEmpty() ? 0 : Double.parseDouble(attendanceGradeStr);
        
        if (attendanceGrade > 100) {
            JOptionPane.showMessageDialog(this, 
                "Attendance Grade cannot exceed 100!",
                "Validation Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        double prelimGrade = computePrelimGrade(lab1, lab2, lab3, prelim, attendanceGrade);
        
        Object[] newRow = {
            studentID, firstName, lastName,
            (int)lab1, (int)lab2, (int)lab3, (int)prelim, (int)attendanceGrade,
            df.format(prelimGrade)
        };
        tableModel.addRow(newRow);
        
        clearFields();
        JOptionPane.showMessageDialog(this, 
            "Student " + firstName + " " + lastName + " added successfully!\nPrelim Grade: " + df.format(prelimGrade),
            "Success", JOptionPane.INFORMATION_MESSAGE);
    }
    
    private void deleteStudent() {
        int selectedRow = table.getSelectedRow();
        
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, 
                "Please select a student to delete!",
                "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String studentName = tableModel.getValueAt(selectedRow, 1) + " " + tableModel.getValueAt(selectedRow, 2);
        
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Delete student: " + studentName + "?",
            "Confirm Delete", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            tableModel.removeRow(selectedRow);
            JOptionPane.showMessageDialog(this, 
                "Student deleted successfully!",
                "Success", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    private void saveToCSV() {
        try {
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setSelectedFile(new java.io.File("student_records.csv"));
            
            int result = fileChooser.showSaveDialog(this);
            if (result == JFileChooser.APPROVE_OPTION) {
                java.io.File file = fileChooser.getSelectedFile();
                
                try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {
                    writer.write("StudentID,first_name,last_name,LAB WORK 1,LAB WORK 2,LAB WORK 3,PRELIM EXAM,ATTENDANCE GRADE,PRELIM GRADE");
                    writer.newLine();
                    
                    for (int i = 0; i < tableModel.getRowCount(); i++) {
                        for (int j = 0; j < tableModel.getColumnCount(); j++) {
                            writer.write(tableModel.getValueAt(i, j).toString());
                            if (j < tableModel.getColumnCount() - 1) {
                                writer.write(",");
                            }
                        }
                        writer.newLine();
                    }
                    
                    JOptionPane.showMessageDialog(this, 
                        "Data saved successfully to:\n" + file.getAbsolutePath(),
                        "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, 
                "Error saving file: " + e.getMessage(),
                "Save Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
